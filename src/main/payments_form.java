     private boolean orderExists(int id) {
 
    config.DBcon dbc = new config.DBcon();
    try {
        // Query the orders table for the specific ID
        java.sql.ResultSet rs = dbc.getData("SELECT o_id FROM tbl_orders WHERE o_id = " + id);
        
        // If rs.next() is true, a record was found
        return rs.next(); 
    } catch (java.sql.SQLException ex) {
        System.out.println("Errors: " + ex.getMessage());
        return false;
    }
}
