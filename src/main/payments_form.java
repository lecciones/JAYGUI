    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
      if (action.equals("Add")) {
        if (validateRegister() == 0) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields correctly!");
            return;
        }

        config.DBcon dbc = new config.DBcon();
        // Updated to match tbl_payment schema from image_0c5447.png
        String query = "INSERT INTO tbl_payment (o_id, payment_method, amount_paid, payment_date) "
                     + "VALUES ('" + oId.getValue() + "', "
                     + "'" + paymentMethod.getSelectedItem() + "', "
                     + "'" + amountpaid.getText() + "', "
                     + "'" + p_date.getText() + "')";

        if (dbc.insertData(query) == 1) {
            JOptionPane.showMessageDialog(null, "Payment Recorded Successfully!");
            this.dispose();
        }

    } else if (action.equals("Update")) {
        if (validateRegister() == 0) return;

        config.DBcon dbc = new config.DBcon();
        // Updated to match tbl_payment schema and use payment_id as the PK
        String query = "UPDATE tbl_payment SET "
                     + "o_id = '" + oId.getValue() + "', "
                     + "payment_method = '" + paymentMethod.getSelectedItem() + "', "
                     + "amount_paid = '" + amountpaid.getText() + "', "
                     + "payment_date = '" + p_date.getText() + "' "
                     + "WHERE payment_id = '" + p_id.getText() + "'";

        dbc.updateData(query);
        JOptionPane.showMessageDialog(null, "Payment Details Updated!");
        this.dispose();
    }
    }//GEN-LAST:event_addMouseClicked

