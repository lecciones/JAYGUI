int validateRegister() {
    try {
        // Updated: Use .getValue() for the quantity spinner
        int quantityValue = (int) quan.getValue();
        if(quantityValue <= 0 || subtotal.getText().equals("0.00")) {
            return 0;
        }
        if((int)pid.getValue() <= 0 || (int)oid.getValue() <= 0) {
            return 0;
        }
        return 1;
    } catch (Exception e) {
        return 0;
    }
}
  
private void calculateSubtotal() {
    try {
        config.DBcon dbc = new config.DBcon();
        int productID = (int) pid.getValue();
        
        java.sql.ResultSet rs = dbc.getData("SELECT p_price FROM tbl_products WHERE p_id = '" + productID + "'");
        
        if (rs.next()) {
            double price = rs.getDouble("p_price");
            // Updated: Use .getValue() and cast to Number/Integer for the spinner
            int quantity = (int) quan.getValue(); 
            double total = quantity * price;
            
            subtotal.setText(String.format("%.2f", total));
        } else {
            subtotal.setText("0.00");
        }
    } catch (Exception e) {
        subtotal.setText("0.00");
    }
}
  private void calculateWithPrice(double price) {
    try {
        // Updated: Use getValue() for JSpinner and cast to Number to get the double value
        double quantity = ((Number) quan.getValue()).doubleValue(); 
        double total = quantity * price;
        subtotal.setText(String.format("%.2f", total));
    } catch (Exception e) {
        // Handles cases where the spinner value might be null or invalid
        subtotal.setText("0.00");
    }
}
  
