    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
 if (action.equals("Add")) {
        if (validateRegister() == 1) {
            config.DBcon dbc = new config.DBcon();

            // Use .getValue() for oid, pid, and quan since they are JSpinners
            String sql = "INSERT INTO tbl_orderDetails (o_id, p_id, quantity, subtotal) "
                       + "VALUES ('" + oid.getValue() + "', '" 
                       + pid.getValue() + "', '" 
                       + quan.getValue() + "', '" 
                       + subtotal.getText() + "')";

            if (dbc.insertData(sql) == 1) {
                javax.swing.JOptionPane.showMessageDialog(null, "Item Added to Order!");
                this.dispose();
            }
        } else {
            javax.swing.JOptionPane.showMessageDialog(null, "Please fill in all fields correctly!");
        }

    } else if (action.equals("Update")) {
        if (validateRegister() == 1) { // Added validation for Update as well
            config.DBcon dbc = new config.DBcon();

            // Updated quan.getText() to quan.getValue()
            String sql = "UPDATE tbl_orderDetails SET "
                       + "o_id = '" + oid.getValue() + "', "
                       + "p_id = '" + pid.getValue() + "', "
                       + "quantity = '" + quan.getValue() + "', "
                       + "subtotal = '" + subtotal.getText() + "' "
                       + "WHERE order_details = '" + id.getText() + "'";

            dbc.updateData(sql);
            javax.swing.JOptionPane.showMessageDialog(null, "Details Updated!");
            this.dispose();
        } else {
             javax.swing.JOptionPane.showMessageDialog(null, "Invalid update data. Please check Quantity.");
        }
    }
    }//GEN-LAST:event_addMouseClicked

