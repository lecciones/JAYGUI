   public void p_spinner() {
    DBcon dbc = new DBcon();
    try {
        java.sql.ResultSet rs = dbc.getData("SELECT c_id FROM tbl_customers");
        
        // Use java.util.List and ArrayList to store the IDs
        java.util.List<Integer> idList = new java.util.ArrayList<>();

        while (rs.next()) {
            idList.add(rs.getInt("c_id"));
        }

        if (!idList.isEmpty()) {
            // SpinnerListModel handles the navigation through your specific IDs
            SpinnerListModel model = new SpinnerListModel(idList);
            cId.setModel(model);
        } else {
            JOptionPane.showMessageDialog(null, "No customers found in database.");
        }
    } catch (java.sql.SQLException e) {
        System.out.println("Error fetching IDs: " + e.getMessage());
    }
}
