    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
     if (action.equals("Add")) {
        if (validateRegister() == 0) {
            JOptionPane.showMessageDialog(null, "Please fill in all fields correctly!");
            return;
        }

        DBcon dbc = new DBcon();
        // Matching your schema: c_id, order_date, amount
        String query = "INSERT INTO tbl_orders (c_id, order_date, amount) "
                     + "VALUES ('" + cId.getValue() + "', '" + orderdate.getText() + "', '" + amount.getText() + "')";
        
        if (dbc.insertData(query) == 1) {
            JOptionPane.showMessageDialog(null, "Order Successfully Placed!");
            close();
        }

    } else if (action.equals("Update")) {
        if (validateRegister() == 0) return;

        DBcon dbc = new DBcon();
        // Matching your schema: c_id, order_date, amount
        String query = "UPDATE tbl_orders SET "
                     + "c_id = '" + cId.getValue() + "', "
                     + "order_date = '" + orderdate.getText() + "', "
                     + "amount = '" + amount.getText() + "' "
                     + "WHERE o_id = '" + or_id.getText() + "'";

        dbc.updateData(query);
        JOptionPane.showMessageDialog(null, "Order Updated Successfully!");
        close();
    }
    }//GEN-LAST:event_addMouseClicked

