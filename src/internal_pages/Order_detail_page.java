    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
   int rowIndex = orderDetail.getSelectedRow();

    if (rowIndex < 0) {
        JOptionPane.showMessageDialog(null, "Please Select an Item!");
    } else {
        TableModel model = orderDetail.getModel();
        Order_details odForm = new Order_details();

        // 1. Primary Key: order_details (Index 0)
        odForm.id.setText(model.getValueAt(rowIndex, 0).toString()); 

        // 2. Foreign Key: o_id (Index 1) - Set Spinner Value
        odForm.oid.setValue(Integer.parseInt(model.getValueAt(rowIndex, 1).toString())); 

        // 3. Foreign Key: p_id (Index 2) - Set Spinner Value
        odForm.pid.setValue(Integer.parseInt(model.getValueAt(rowIndex, 2).toString())); 

        // 4. Quantity: (Index 3) - Set Spinner Value (Now a JSpinner)
        odForm.quan.setValue(Integer.parseInt(model.getValueAt(rowIndex, 3).toString())); 

        // 5. Financial: subtotal (Index 4)
        odForm.subtotal.setText(model.getValueAt(rowIndex, 4).toString()); 

        odForm.setVisible(true);
        odForm.action = "Update";
        odForm.st_label.setText("UPDATE");
        
        // Dispose of the current page
        JFrame mainFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        mainFrame.dispose();
    }
       
    }//GEN-LAST:event_editMouseClicked

