public void displayData() {
    config.DBcon cc = new config.DBcon();
    
    // This query pulls the Product Name from the products table 
    // while getting the rest from order details
    String query = "SELECT od.order_details as 'Detail ID', "
                 + "od.o_id as 'Order ID', "
                 + "p.p_name as 'Product Name', " // Assuming your product table has p_name
                 + "od.quantity as 'Quantity', "
                 + "od.subtotal as 'Subtotal' "
                 + "FROM tbl_orderDetails od "
                 + "JOIN tbl_products p ON od.p_id = p.p_id";
                 
    cc.displayData(query, orderDetail); 
}


