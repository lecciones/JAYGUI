    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
    int rowIndex = payments_table.getSelectedRow();

    if (rowIndex < 0) {
        JOptionPane.showMessageDialog(null, "Please Select a Payment Record!");
    } else {
        TableModel model = payments_table.getModel();
        payments_form of = new payments_form();

        // 1. Map Table Data to the payments_form components
        of.p_id.setText(model.getValueAt(rowIndex, 0).toString());  // Payment ID
        of.oId.setValue(model.getValueAt(rowIndex, 1));           // Order ID (Spinner)
        
        // Handle ComboBox for Payment Method
        String method = model.getValueAt(rowIndex, 2).toString();
        of.paymentMethod.setSelectedItem(method);                 // Method (ComboBox)

        of.amountpaid.setText(model.getValueAt(rowIndex, 3).toString()); // Amount
        of.p_date.setText(model.getValueAt(rowIndex, 4).toString());    // Date

        // 2. Configure form for Update mode
        of.setVisible(true);
        of.action = "Update";
        of.st_label.setText("UPDATE");

        // 3. Close the current dashboard window
        JFrame mainFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        mainFrame.dispose();

        of.pack();
        of.setLocationRelativeTo(null);
    }
    }//GEN-LAST:event_editMouseClicked

