    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        int rowIndex = payments_table.getSelectedRow();

        if(rowIndex < 0){
            JOptionPane.showMessageDialog(null, "Please select an order from the table!");
        } else {
            TableModel model = payments_table.getModel();
            Object value = model.getValueAt(rowIndex, 0); // Gets o_id from the first column
            String id = value.toString();

            // Customizing the message to be Order-specific
            int a = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete Order ID: " + id + "?", "Delete Confirmation", JOptionPane.YES_NO_OPTION);

            if(a == JOptionPane.YES_OPTION){
                DBcon dbc = new DBcon();
                try {
                    int p_id = Integer.parseInt(id);
                    // Matches your DB schema: table "tbl_orders" and primary key "o_id"
                    dbc.deleteData(p_id, "tbl_payment", "payment_id");

                    // Refresh the table to show the record is gone
                    displayData();
                } catch (NumberFormatException e) {
                    JOptionPane.showMessageDialog(null, "Invalid Order ID format.");
                }
            }
        }
    }//GEN-LAST:event_deleteMouseClicked

